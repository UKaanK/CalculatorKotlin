package com.example.dortislemkotlin

class DortIslem {

    fun topla(say1: Int, say2: Int): Int {
        return say1+say2
    }

    fun carp(say1: Int,say2: Int): Int{
        return say1*say2
    }

    fun bol(say1: Int,say2: Int): Int{
        return say1/say2
    }

    fun cikar(say1: Int,say2: Int): Int{
        return say1-say2
    }
}